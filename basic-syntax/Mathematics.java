public class Mathematics {
    public static void main(String[] args) {
        System.out.println(Math.max(5, 10)); // Math.max(5, 10);
        System.out.println(Math.min(5, 10)); // Math.min(5, 10);
        System.out.println(Math.sqrt(64)); // Returns the square root
        System.out.println(Math.abs(-5.6)); // Returns the absolute value
        System.out.println(Math.pow(2, 6)); // Returns the power
        System.out.println(Math.random()); // Returns a random number
        System.out.println(Math.round(5.6)); // Returns a rounded number
        int randomNum = (int)(Math.random() * 101);  // 0 to 100
        System.out.println(randomNum);

    }
}
